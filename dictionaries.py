#Dictionaries - a collection of key value pairs. Indexed by keys, which can be of any type (string/numeric data type)
#Dictionries are mutable - you can add new value key pairs or modify existing ones.

my_dict = {'apple': 3, 'banana': 5, 'orange': 2} #apple, banana, orange - keys.. 3, 5, 2 - their values.

print(my_dict['apple']) 

my_dict['grapes'] = 7 #Adding a value 
print(my_dict)

my_dict['banana'] = 4
print(my_dict)

